class Calculadora {
    fun soma(x: Double, y: Double): Double {
        return x + y
    }

    fun subtração(x: Double, y: Double): Double {
        return x - y
    }

    fun multiplicação(x: Double, y: Double): Double {
        return x * y
    }

    fun divisão(x: Double, y: Double): Double {
        return x / y
    }
}